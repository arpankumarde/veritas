"""Mini-tool endpoints — AI detection and plagiarism checking."""

import asyncio
import re

from fastapi import APIRouter, HTTPException
from pydantic import BaseModel

from claude_agent_sdk import (
    AssistantMessage,
    ClaudeAgentOptions,
    ResultMessage,
    TextBlock,
    query,
)

from engine.agents.base import _get_api_key
from engine.logging_config import get_logger
from engine.tools.bright_data import BrightDataClient, ScrapedPage

logger = get_logger(__name__)

router = APIRouter(prefix="/api/tools", tags=["tools"])

# ---------------------------------------------------------------------------
# Request / response models
# ---------------------------------------------------------------------------

class AnalyzeRequest(BaseModel):
    url: str
    mode: str = "text"  # "text" | "image" | "auto"


class DetectionResult(BaseModel):
    url: str
    verdict: str            # "ai_generated" | "human_written" | "mixed" | "inconclusive"
    confidence: float       # 0-1
    summary: str
    indicators: list[str]
    content_type: str       # "text" | "image"
    content_preview: str    # first ~300 chars of scraped content


class PlagiarismRequest(BaseModel):
    url: str


class PlagiarismMatch(BaseModel):
    passage: str
    source_url: str
    source_title: str
    similarity: str  # "high" | "moderate" | "low"


class PlagiarismResult(BaseModel):
    url: str
    originality_score: float  # 0-1 (1 = fully original)
    verdict: str              # "original" | "partially_plagiarized" | "heavily_plagiarized"
    summary: str
    matches: list[PlagiarismMatch]
    content_preview: str


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _build_env() -> dict[str, str]:
    """Build env dict with API key for Claude Agent SDK calls."""
    env: dict[str, str] = {}
    if api_key := _get_api_key():
        env["ANTHROPIC_API_KEY"] = api_key
    return env


async def _call_claude(prompt: str, model: str = "sonnet", max_tokens: int = 1024) -> str:
    """Call Claude via the Agent SDK (same auth as the research engine)."""
    env = _build_env()
    options = ClaudeAgentOptions(
        model=model,
        max_turns=1,
        allowed_tools=[],
        system_prompt="You are an analysis assistant. Follow instructions precisely and output in the exact format requested.",
        env=env,
    )

    response_text = ""

    async def _run():
        nonlocal response_text
        async for message in query(prompt=prompt, options=options):
            if isinstance(message, AssistantMessage):
                for block in message.content:
                    if isinstance(block, TextBlock):
                        response_text += block.text

    await asyncio.wait_for(_run(), timeout=120)
    return response_text


def _get_bright_data() -> BrightDataClient:
    return BrightDataClient()


async def _scrape_url(url: str) -> ScrapedPage:
    """Scrape a URL via Bright Data and return markdown content."""
    bd = _get_bright_data()
    try:
        page = await bd.scrape_page(url, fmt="markdown")
        if page.ok:
            return page
        return ScrapedPage(url=url, content="", ok=False, error=page.error or "Failed to scrape page")
    except Exception as e:
        logger.warning("Scrape failed for %s: %s", url, e)
        return ScrapedPage(url=url, content="", ok=False, error=str(e))


