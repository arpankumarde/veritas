@router.post("/detect-ai", response_model=DetectionResult)
async def detect_ai_content(req: AnalyzeRequest):
    """Detect whether content at a URL is AI-generated."""
    page = await _scrape_url(req.url)
    if not page.ok or not page.content.strip():
        raise HTTPException(status_code=422, detail=f"Could not scrape content from URL: {page.error or 'empty page'}")

    content = page.content[:12000]  # cap for context window
    preview = page.content[:300]

    prompt = f"""Analyze the following web content and determine whether it was written by a human or generated by AI (like ChatGPT, Claude, etc.).

URL: {req.url}

CONTENT:
{content}

Provide your analysis in EXACTLY this format (no markdown fences):
VERDICT: <ai_generated|human_written|mixed|inconclusive>
CONFIDENCE: <0.0 to 1.0>
SUMMARY: <2-3 sentence explanation>
INDICATORS:
- <indicator 1>
- <indicator 2>
- <indicator 3>
- <indicator 4>
- <indicator 5>

Focus on: repetitive sentence structures, lack of personal voice, hedging language ("It's important to note"), formulaic transitions, unusual uniformity in paragraph length, lack of specific personal experience, generic examples, and characteristic AI phrasings."""

    try:
        text = await _call_claude(prompt, model="sonnet")

        verdict = "inconclusive"
        confidence = 0.5
        summary = ""
        indicators: list[str] = []

        for line in text.split("\n"):
            line = line.strip()
            if line.startswith("VERDICT:"):
                v = line.split(":", 1)[1].strip().lower()
                if v in ("ai_generated", "human_written", "mixed", "inconclusive"):
                    verdict = v
            elif line.startswith("CONFIDENCE:"):
                try:
                    confidence = float(line.split(":", 1)[1].strip())
                    confidence = max(0.0, min(1.0, confidence))
                except ValueError:
                    pass
            elif line.startswith("SUMMARY:"):
                summary = line.split(":", 1)[1].strip()
            elif line.startswith("- "):
                indicators.append(line[2:].strip())

        return DetectionResult(
            url=req.url,
            verdict=verdict,
            confidence=confidence,
            summary=summary or "Analysis completed.",
            indicators=indicators[:8],
            content_type="text",
            content_preview=preview,
        )
    except Exception as e:
        logger.error("AI detection failed: %s", e)
        raise HTTPException(status_code=500, detail=f"Analysis failed: {str(e)}")


