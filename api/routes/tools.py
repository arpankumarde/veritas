@router.post("/check-plagiarism", response_model=PlagiarismResult)
async def check_plagiarism(req: PlagiarismRequest):
    """Check content at a URL for potential plagiarism."""
    page = await _scrape_url(req.url)
    if not page.ok or not page.content.strip():
        raise HTTPException(status_code=422, detail=f"Could not scrape content from URL: {page.error or 'empty page'}")

    content = page.content[:12000]
    preview = page.content[:300]

    # Step 1: Extract key passages for search
    extract_prompt = f"""Extract 5 distinctive passages (1-2 sentences each) from this content that would be most useful for plagiarism detection. Choose passages with specific claims, unique phrasing, or factual statements — not generic introductions.

CONTENT:
{content}

Return ONLY the passages, one per line, prefixed with PASSAGE: (no numbering, no quotes)."""

    try:
        extract_text = await _call_claude(extract_prompt, model="haiku")
        passages = []
        for line in extract_text.split("\n"):
            line = line.strip()
            if line.startswith("PASSAGE:"):
                p = line.split(":", 1)[1].strip().strip('"').strip("'")
                if len(p) > 20:
                    passages.append(p)
        passages = passages[:5]

        if not passages:
            # Fallback: split content into chunks
            sentences = re.split(r'(?<=[.!?])\s+', content)
            passages = [s for s in sentences if len(s) > 40][:5]

    except Exception as e:
        logger.error("Passage extraction failed: %s", e)
        raise HTTPException(status_code=500, detail=f"Analysis failed: {str(e)}")

    # Step 2: Search for each passage
    bd = _get_bright_data()
    matches: list[PlagiarismMatch] = []

    for passage in passages:
        try:
            search_query = f'"{passage[:80]}"'
            results = await bd.search(search_query, num=5, engine="google")
            for r in results:
                if r.url != req.url and r.url not in [m.source_url for m in matches]:
                    matches.append(PlagiarismMatch(
                        passage=passage[:200],
                        source_url=r.url,
                        source_title=r.title,
                        similarity="high" if passage[:40].lower() in r.description.lower() else "moderate",
                    ))
        except Exception as e:
            logger.warning("Search failed for passage: %s", e)

    # Step 3: Synthesize verdict
    synth_prompt = f"""You are a plagiarism analysis system. Given the original content and search results, determine the originality.

ORIGINAL URL: {req.url}
CONTENT PREVIEW:
{content[:3000]}

SEARCH MATCHES FOUND: {len(matches)}
{chr(10).join(f'- "{m.passage[:80]}..." found at {m.source_url} ({m.similarity} similarity)' for m in matches[:10])}

Provide your analysis in EXACTLY this format (no markdown fences):
ORIGINALITY: <0.0 to 1.0 where 1.0 is fully original>
VERDICT: <original|partially_plagiarized|heavily_plagiarized>
SUMMARY: <2-3 sentence assessment>

Consider: Are the matches from the same author? Are they common phrases? Is this content clearly copied or just covering the same topic?"""

    try:
        synth_text = await _call_claude(synth_prompt, model="sonnet")

        originality = 0.8
        verdict = "original"
        summary = ""

        for line in synth_text.split("\n"):
            line = line.strip()
            if line.startswith("ORIGINALITY:"):
                try:
                    originality = float(line.split(":", 1)[1].strip())
                    originality = max(0.0, min(1.0, originality))
                except ValueError:
                    pass
            elif line.startswith("VERDICT:"):
                v = line.split(":", 1)[1].strip().lower()
                if v in ("original", "partially_plagiarized", "heavily_plagiarized"):
                    verdict = v
            elif line.startswith("SUMMARY:"):
                summary = line.split(":", 1)[1].strip()

        return PlagiarismResult(
            url=req.url,
            originality_score=originality,
            verdict=verdict,
            summary=summary or "Analysis completed.",
            matches=matches[:10],
            content_preview=preview,
        )
    except Exception as e:
        logger.error("Plagiarism synthesis failed: %s", e)
        raise HTTPException(status_code=500, detail=f"Analysis failed: {str(e)}")
